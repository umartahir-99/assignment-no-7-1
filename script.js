// Write a program that asks the user to input either 1 (turn on) or 0 (turn off).


var userInput =prompt("Enter a number:")
if(userInput == 1){
   console.log("The light is Turn ON");
  
}
else if(userInput == 0){
   console.log("The light is Turn Off");
 
}
else{
    console.log("Invalid Input")
 
}

